package formSubmit;


public class Content {
	private long id;
	private String name;
	private String st;
	
	
	public Content() {
		this.id = 5;
		this.name = "name";
		this.st = "st";
	}
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getSt() {
		return st;
	}
	public void setSt(String st) {
		this.st = st;
	}
	
	
}
